AppBar appBar(titleOfAppbar) {
  return AppBar(
    centerTitle: true,
    flexibleSpace: Container(
        decoration: BoxDecoration(
      gradient: LinearGradient(
          begin: Alignment.topCenter,
          end: Alignment.bottomCenter,
          colors: <Color>[AppColors.cardBg1, AppColors.cardBg2]),
    )),
    title: Text(
      titleOfAppbar,
      maxLines: 1,
      style: TextStyle(
        fontWeight: FontWeight.w600,
        color: AppColors.white,
        fontSize: Dimens.font_16sp,
      ),
    ),
  );
}

AppBar transParentAppbar(titleOfAppbar) {
  return AppBar(
    centerTitle: false,
    flexibleSpace: Container(
        decoration: BoxDecoration(
      gradient: LinearGradient(
          begin: Alignment.topCenter,
          end: Alignment.bottomCenter,
          colors: <Color>[AppColors.white, AppColors.white]),
    )),
    title: Text(
      titleOfAppbar,
      style: TextStyle(
        fontWeight: FontWeight.w600,
        color: AppColors.cardBg1,
        fontSize: Dimens.font_16sp,
      ),
    ),
  );
}

Widget rupeeIcon(
    {required Color color,
    required String label,
    required Color textColor,
    bool? boldfont,
    required double fontSize}) {
  return Row(
    children: [
      SizedBox(
        width: fontSize * 0.8,
        height: fontSize * 0.8,
        child: Image.asset(
          AppImages.rupees_icon,
          color: color,
        ),
      ),
      const SizedBox(width: 4.0),
      Text(
        label,
        style: AppStyle.shortHeading.copyWith(
          fontSize: fontSize,
          fontWeight: boldfont == true ? FontWeight.w600 : null,
          color: textColor,
        ),
      ),
    ],
  );
}

